import Phaser from "phaser";
import Enemies from "./Enemies";
import Player from "./Player";

export default class EnemiesHitbox extends Phaser.GameObjects.Rectangle {
  constructor(scene, squirrels) {
    super(scene, squirrels.x, squirrels.y);
    scene.add.existing(this);
    scene.physics.add.existing(this);
    this.body.setImmovable(true);
    this.body.allowGravity = false;

    this.squirrels = squirrels;

    //this.facingDirection = null;
    //this.damageAmount = 100;
  }

  update() {
    this.x = this.squirrels.x;
    this.y = this.squirrels.y;
  }
}
